Η πιο απλή, πιο κλασική, πιο «πάμε για μια μπύρα» επιλογή. Αν η μπύρα ήταν άνθρωπος, η lager θα ήταν ο φίλος που πάντα φέρνει πατατάκια στα πάρτι. Ήρεμη, δροσερή, δεν κρίνει. Επίσης, πίνεται με τρομακτικούς ρυθμούς όταν κάνει ζέστη.
🍺 Weiss
Μπύρα με γεύση μπανάνας και εμφάνιση που θυμίζει θολό νερό από νιπτήρα. Η weiss είναι σαν το πρώτο φιλί: περίεργη, αμήχανη, και μετά δεν την ξεχνάς ποτέ. Πίνεται με λεμόνι (αν είσαι στην παραλία) ή χωρίς λεμόνι (αν θες να θεωρηθείς σοβαρός).
🍺 Stout / Porter
